"""Pydantic data models for the Fraud Analytics agent.

FraudAnalysisOutput  – structured verdict produced by the agent per case.
CaseRecord           – investigation case with seed transaction + ground truth.
AccuracyResult       – returned by the check_accuracy tool.
"""

from __future__ import annotations

from enum import Enum
from typing import Optional

from pydantic import BaseModel, Field


class FraudPattern(str, Enum):
    """Known fraud pattern types the agent may detect."""

    CARD_NOT_PRESENT = "card_not_present"
    ACCOUNT_TAKEOVER = "account_takeover"
    IDENTITY_THEFT = "identity_theft"
    SMURFING = "smurfing"
    MERCHANT_FRAUD = "merchant_fraud"
    UNUSUAL_VELOCITY = "unusual_velocity"
    GEO_ANOMALY = "geo_anomaly"
    UNKNOWN = "unknown"
    NONE = "none"  # Used when is_fraud=False


class FraudAnalysisOutput(BaseModel):
    """Structured output produced by the Fraud Analytics agent for each case.

    All five fields must be populated. The agent is instructed to return a
    JSON block matching this schema as its final response.
    """

    is_fraud: bool = Field(
        description="True if the evidence supports a fraudulent transaction, False otherwise.",
    )
    fraud_pattern: FraudPattern = Field(
        default=FraudPattern.NONE,
        description=(
            "Detected fraud pattern type. Set to 'none' when is_fraud=False, "
            "'unknown' when fraud is detected but pattern is unclear."
        ),
    )
    flagged_transaction_ids: list[str] = Field(
        default_factory=list,
        description="List of transaction IDs flagged as suspicious during investigation.",
    )
    confidence_score: float = Field(
        ge=0.0,
        le=1.0,
        description="Agent confidence in the is_fraud verdict, from 0.0 (uncertain) to 1.0 (certain).",
    )
    explanation: str = Field(
        description=(
            "Narrative explanation of the investigation findings. Should reference "
            "specific SQL query results and tool outputs used to reach the verdict."
        ),
    )


class CaseRecord(BaseModel):
    """A single fraud investigation case.

    Generated by `cli.py create-cases` and consumed by the batch runner
    and evaluate.py. Matches the structure used by aml_investigation.
    """

    case_id: str = Field(description="Unique case identifier, e.g. 'CASE_0001'.")
    seed_transaction_id: str = Field(
        description="The transaction ID where the investigation begins.",
    )
    client_id: str = Field(description="Client ID associated with the seed transaction.")
    card_id: str = Field(description="Card ID associated with the seed transaction.")
    window_start: str = Field(
        description="ISO-format datetime string — earliest transaction the agent should consider.",
    )
    window_end: str = Field(
        description="ISO-format datetime string — latest transaction (= seed transaction date).",
    )
    trigger_label: str = Field(
        description=(
            "Human-readable label explaining why this case was flagged, e.g. "
            "'high-amount transaction', 'geo anomaly alert', 'rule-based review'."
        ),
    )
    ground_truth_is_fraud: bool = Field(
        description="Ground-truth fraud label from the dataset's is_fraud column.",
    )
    ground_truth_pattern: FraudPattern = Field(
        default=FraudPattern.UNKNOWN,
        description="Best-guess pattern label derived from dataset features (used as soft ground truth).",
    )

    # Populated by the batch runner after the agent runs
    output: Optional[FraudAnalysisOutput] = Field(
        default=None,
        description="Agent's structured output. None if not yet processed.",
    )
    error: Optional[str] = Field(
        default=None,
        description="Error message if the agent failed on this case.",
    )


class AccuracyResult(BaseModel):
    """Returned by the check_accuracy tool to the agent."""

    transaction_id: str
    ground_truth_is_fraud: bool
    agent_prediction: bool
    match: bool = Field(description="True if agent prediction matches ground truth.")
    message: str = Field(description="Human-readable summary for the agent to reason about.")
